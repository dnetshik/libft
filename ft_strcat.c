#include "libft.h"

char	*ft_strcat(char *s1, char *s2)
{
	int len1;
	int i;

	len1 = ft_strlen(s1);
	i = 0;
	while (s2[i] != '\0')
	{
		s1[i + len1] = s2[i];
		i++;
	}
	s1[len1 + i] = '\0';
	return (s1);
}
